    private void jPanel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel2MouseClicked
        String BID1 = BID.getText();
        String name = Bname.getText();
        int Quan= Integer.parseInt(Quantity.getText());
        Book bk;
        if(Quan>0){
            bk = new Book(BID1,name,true,Quan);
        }else{
            bk = new Book(BID1,name,false,Quan);
        }
        Admin.addBook(bk);
        new AdminInter().setVisible(true);
        this.dispose();
    }//GEN-LAST:event_jPanel2MouseClicked

